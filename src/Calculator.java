public class Calculator {

    static void toplama (int a, int b) {
        System.out.println(a+b);
    }
    static void cixma (int a, int b) {
        System.out.println(a-b);
    }
    static void vurma (int a, int b) {
        System.out.println(a*b);
    }
    static void bolme (int a, int b) {
        System.out.println(a/b);
    }
}
